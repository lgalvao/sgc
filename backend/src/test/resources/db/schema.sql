-- Tabelas do Módulo de Diagnóstico
create table if not exists sgc.diagnostico
(
    codigo                  bigint generated by default as identity,
    subprocesso_codigo      bigint      not null unique,
    situacao                varchar(50) not null check (situacao in ('EM_ANDAMENTO', 'CONCLUIDO', 'VALIDADO', 'HOMOLOGADO')),
    data_conclusao          timestamp(6),
    justificativa_conclusao text,
    primary key (codigo)
);

create table if not exists sgc.avaliacao_servidor
(
    codigo             bigint generated by default as identity,
    diagnostico_codigo bigint       not null,
    servidor_titulo    varchar(255) not null,
    competencia_codigo bigint       not null,
    importancia        varchar(10) check (importancia in ('NA', 'N1', 'N2', 'N3', 'N4', 'N5', 'N6')),
    dominio            varchar(10) check (dominio in ('NA', 'N1', 'N2', 'N3', 'N4', 'N5', 'N6')),
    gap                int,
    observacoes        text,
    situacao_servidor  varchar(50)  not null check (situacao_servidor in (
                                                                          'AUTOAVALIACAO_NAO_REALIZADA',
                                                                          'AUTOAVALIACAO_CONCLUIDA',
                                                                          'CONSENSO_CRIADO',
                                                                          'CONSENSO_APROVADO',
                                                                          'AVALIACAO_IMPOSSIBILITADA'
        )),
    primary key (codigo),
    unique (diagnostico_codigo, servidor_titulo, competencia_codigo)
);

create table if not exists sgc.ocupacao_critica
(
    codigo               bigint generated by default as identity,
    diagnostico_codigo   bigint       not null,
    servidor_titulo      varchar(255) not null,
    competencia_codigo   bigint       not null,
    situacao_capacitacao varchar(10)  not null check (situacao_capacitacao in ('NA', 'AC', 'EC', 'C', 'I')),
    primary key (codigo),
    unique (diagnostico_codigo, servidor_titulo, competencia_codigo)
);

